/** Tasks grouped by status for a given project */
export function getDemoTasksGrouped(projectId: string, sprintId?: string | null) {
  let tasks = DEMO_TASKS.filter((t) => t.projectId === projectId);
  if (sprintId) {
    tasks = tasks.filter((t) => t.sprintId === sprintId);
  }
  const grouped: Record<string, DemoTask[]> = {
    BACKLOG: [],
    TODO: [],
    IN_PROGRESS: [],
    IN_REVIEW: [],
    DONE: [],
  };
  for (const task of tasks) {
    grouped[task.status].push(task);
  }
  return { tasks, grouped, nextCursor: null, hasMore: false };
}
