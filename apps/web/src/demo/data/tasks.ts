import type { TaskStatus, TaskPriority } from '@/lib/constants';
import { DEMO_USERS } from './users';
import { DEMO_LABELS } from './labels';

export interface DemoTaskLabel {
  taskId: string;
  labelId: string;
  label: {
    id: string;
    workspaceId: string;
    name: string;
    color: string;
  };
}

export interface DemoTaskUser {
  id: string;
  email: string;
  firstName: string;
  lastName: string;
  avatarUrl?: string;
  timezone?: string;
}

export interface DemoTask {
  id: string;
  projectId: string;
  sprintId: string | null;
  parentTaskId?: string | null;
  title: string;
  description: string | null;
  status: TaskStatus;
  priority: TaskPriority;
  assigneeId: string | null;
  reporterId: string;
  position: number;
  dueDate: string | null;
  storyPoints: number | null;
  completedAt: string | null;
  createdAt: string;
  updatedAt: string;
  deletedAt?: string | null;
  assignee?: DemoTaskUser | null;
  reporter?: DemoTaskUser;
  labels?: DemoTaskLabel[];
  _count?: { comments: number; childTasks?: number };
}

function user(id: string): DemoTaskUser {
  const u = DEMO_USERS.find((u) => u.id === id)!;
  return { id: u.id, email: u.email, firstName: u.firstName, lastName: u.lastName, avatarUrl: u.avatarUrl, timezone: u.timezone };
}

function label(taskId: string, labelId: string): DemoTaskLabel {
  const l = DEMO_LABELS.find((l) => l.id === labelId)!;
  return { taskId, labelId, label: { id: l.id, workspaceId: l.workspaceId, name: l.name, color: l.color } };
}

